# 3)CODING TYPE QUESTION
#
# Print the Pattern
#
#     1
#    123
#   12345
#  1234567
# 123456789

# ans:

rows = 5

for i in range(1, rows+1):
    cn =1
    for j in range(rows-i):
        print(" ", end="")
    for j in range(2*i-1):
        print(cn, end="")
        cn+=1
    print()

